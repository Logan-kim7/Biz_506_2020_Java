package com.biz.hello;

public class Var_04 {
	
	public static void main(String[] args) {
		// 변수를 선언만 한 상태
        int num1;
        int num2;
        
        
        // 선언된 변수에 값 할당
        num1 = 30;
        //선언된 변수 clear
        num2 = 0;
        
        System.out.println(num1);
        System.out.println(num2);
        
	}

}
