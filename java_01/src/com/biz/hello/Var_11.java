package com.biz.hello;

public class Var_11 {
	public static void main(String[] args) {
		
		int num1 = 1;
		
		// num1에 1을 추가하라
		num1 = num1 + 1;
		num1 += 1;
		
		// 추가되는 값이 1일 경우는 아래와 같은 코드로 사용가능
		num1++;
		++num1;
			
		int num2 = 10;
		
		//num2에 12를 추가하라
		num2 = num2 + 12;
		num2 += 12;
		
	}

}
